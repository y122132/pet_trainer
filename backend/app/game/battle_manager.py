# --- 배틀 매니저 (Battle Manager) ---
# 이 파일은 플레이어 간 혹은 플레이어와 NPC 간의 배틀 세션을 관리합니다.
# 특히 '친구 기능'과 연동하여 두 유저 사이의 턴 동기화와 데이터 패킷 전송을 담당합니다.
#
# 추후 구현 계획:
# 1. BattleSession_Management: 유저 ID와 친구 ID를 매칭하여 고유 배틀 룸 생성
# 2. Turn_Logic: 양측의 입력을 받아 '계산기'를 호출한 뒤 행동 순서대로 결과 집계
# 3. State_Sync: 실시간 웹소켓을 통해 배틀 로그(누가 어떤 기술을 썼고 얼마의 데미지를 입었는지) 전송
# 4. Result_Processor: 승패 판정 후 캐릭터의 경험치, 스탯 포인트, 친구 간 친밀도 반영