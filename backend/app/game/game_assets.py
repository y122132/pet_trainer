# --- 게임 에셋 (Game Assets) ---
# 이 파일은 배틀 시스템에서 사용하는 모든 정적 데이터를 정의합니다.
# 스킬 정보, 타입 상성표, 성격 및 컨디션에 따른 보정치 등을 관리하며
# 프론트엔드와 공유되는 핵심 데이터의 기준점이 됩니다.
#
# 추후 구현 계획:
# 1. Skill_Database: 각 스킬의 이름, 위력, 명중률, 속성(불, 물, 풀 등) 정의
# 2. Type_Chart: 포켓몬 스타일의 속성 간 상성 계수(2배, 0.5배 등) 정의
# 3. Personality_Modifiers: 캐릭터 성격(용감함, 겁쟁이 등)에 따른 스탯 가중치 설정
# 4. Battle_Items: 전투 중 사용 가능한 아이템의 효과 데이터 정의